package main

import "fmt"

func main() {
	for i := 1; i <= 10; i++ {
		if i == 0 {
			fmt.Println("Zero")
		} else if i == 1 {
			fmt.Println("Um")
		} else if i == 2 {
			fmt.Println("Dois")
		} else if i == 3 {
			fmt.Println("Tres")
		} else if i == 4 {
			fmt.Println("Quatro")
		} else if i == 5 {
			fmt.Println("Cinco")
		} else if i == 6 {
			fmt.Println("Seis")
		} else if i == 7 {
			fmt.Println("Sete")
		} else if i == 8 {
			fmt.Println("Oito")
		} else if i == 9 {
			fmt.Println("Nove")
		} else if i == 10 {
			fmt.Println("Dez")
		}
	}
}
